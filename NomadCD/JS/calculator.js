const calculator = {
    add: function(a,b) {
        console.log(a+b);
    },
    minus: function(a,b) {
        console.log(a-b);
    },
    divide: function(a,b) {
        console.log(a/b);
    },
    multi: function(a,b) {
        console.log(a*b);
    },
    x: function(a,b) {
        console.log(a**b);
    },
};

calculator.add(5, 4);
calculator.minus(5, 4);
calculator.divide(5, 4);
calculator.multi(5, 2);
calculator.x(5, 2);

